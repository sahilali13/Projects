    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airteldb","root","")) {
                Statement stmt=con.createStatement();
                int flag=0;
                ResultSet rs=stmt.executeQuery("select * from user");
                if(username.getText().equals(""))
                    flag=1;
                else if(pwd.getText().equals(""))
                    flag=2;
                else
                    while(rs.next())
                        if(username.getText().equals(rs.getString(2)))
                            if(pwd.getText().equals(rs.getString(3))){
                                flag=5;
                                break;
                            } else {
                                flag=4;
                                break;
                            }
                        else
                            flag=3;
                JFrame frame = new JFrame("");
                switch(flag){
                    case 1:
                        JOptionPane.showMessageDialog(frame,"Please Enter Username","Empty Field",JOptionPane.WARNING_MESSAGE);
                        break;
                    case 2:
                        JOptionPane.showMessageDialog(frame,"Please Enter Password","Empty Field",JOptionPane.WARNING_MESSAGE);
                        break;
                    case 3:
                        JOptionPane.showMessageDialog(frame,"Incorrect Username","Error",JOptionPane.ERROR_MESSAGE);
                        break;
                    case 4:
                        JOptionPane.showMessageDialog(frame,"Incorrect Password","Error",JOptionPane.ERROR_MESSAGE);
                        break;
                    case 5:
                        JOptionPane.showMessageDialog(frame,"Success","Success",JOptionPane.PLAIN_MESSAGE);
                        name=username.getText();
                        main2 second=new main2();
                        second.setVisible(true);
                        break;
                }
            }
        }catch(HeadlessException | SQLException e){ 
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

